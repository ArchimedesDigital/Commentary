this.Commenters = new Meteor.Collection('commenters');

Schemas.Commenters = new SimpleSchema({

  createdAt: {
    type: Date,
    autoValue: function() {
      if (this.isInsert) {
        return new Date();
      }
    }
  },

  updatedAt: {
    type: Date,
    optional: true,
    autoValue: function() {
      if (this.isUpdate) {
        return new Date();
      }
    }
  }

});

Commenters.attachSchema(Schemas.Commenters);
